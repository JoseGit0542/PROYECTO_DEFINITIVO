package arquitectura.dominio;

public class Plataforma {

    private int id;
    private String nombre;

    // Constructor
    public Plataforma(int id, String nombre) {
        this.id = id;
        this.nombre = nombre;
    }

    // Getters y setters
    public int getId() {
        return id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    @Override
    public String toString() {
        return id + ";" + nombre;
    }
}
